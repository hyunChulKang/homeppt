package vo;

public class HotelVO {
	
	private String hotelName;
	private String hotelInfo;
	private String hotelGrade;
	private String hotelAdress;
	private String hotelHp;
	private int hotelId;
	private int localId;
	public String getHotelName() {
		return hotelName;
	}
	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}
	public String getHotelInfo() {
		return hotelInfo;
	}
	public void setHotelInfo(String hotelInfo) {
		this.hotelInfo = hotelInfo;
	}
	public String getHotelGrade() {
		return hotelGrade;
	}
	public void setHotelGrade(String hotelGrade) {
		this.hotelGrade = hotelGrade;
	}
	public String getHotelAdress() {
		return hotelAdress;
	}
	public void setHotelAdress(String hotelAdress) {
		this.hotelAdress = hotelAdress;
	}
	public String getHotelHp() {
		return hotelHp;
	}
	public void setHotelHp(String hotelHp) {
		this.hotelHp = hotelHp;
	}
	public int getHotelId() {
		return hotelId;
	}
	public void setHotelId(int hotelId) {
		this.hotelId = hotelId;
	}
	public int getLocalId() {
		return localId;
	}
	public void setLocalId(int localId) {
		this.localId = localId;
	}
	


}